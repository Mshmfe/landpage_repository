document.addEventListener('DOMContentLoaded', function() {
    // Set initial language from localStorage or default to 'en'
    currentLanguage = localStorage.getItem('language') || 'en';

    // Initial setup
    updateTextDirection();
    updateContent();
    updateButtonText();

    // Add counter functionality
    const targetNumber = 95000; // الرقم المستهدف
    const duration = 10000; // مدة الحركة (بالميلي ثانية)
    const dynamicCounterElement = document.getElementById('dynamicCounter');

    // التحقق من وجود العنصر
    if (!dynamicCounterElement) {
        console.error('Dynamic counter element not found!');
        return;
    }

    let currentNumber = 0; // الرقم الحالي
    const increment = Math.ceil(targetNumber / (duration / 100)); // مقدار الزيادة في كل خطوة

    const updateCounter = () => {
        console.log('Updating counter...'); // تسجيل بدء تحديث العداد
        currentNumber += increment;
        if (currentNumber >= targetNumber) {
            currentNumber = targetNumber; // التأكد من الوصول للرقم المستهدف
            dynamicCounterElement.textContent = currentNumber.toLocaleString(); // تحديث الرقم المعروض مع التنسيق
            updateVisitorText(); // تحديث النص بناءً على اللغة
            console.log('Counter updated to:', currentNumber); // تسجيل الرقم المحدث
            return; // إنهاء الدالة
        }
        dynamicCounterElement.textContent = currentNumber.toLocaleString(); // تحديث الرقم المعروض مع التنسيق
        requestAnimationFrame(updateCounter); // استدعاء الدالة مرة أخرى
    };

    requestAnimationFrame(updateCounter); // بدء العداد

    // تعيين النصوص في القائمة المنسدلة
    const dropdownLinks = document.querySelectorAll('.dropdown-content a');
    dropdownLinks.forEach(link => {
        const translationKey = link.getAttribute('data-translate');
        if (translations[translationKey]) {
            link.textContent = translations[translationKey][currentLanguage];
        } else {
            console.warn(`Translation key ${translationKey} not found`);
        }
    });

    // Add event listener for login button to toggle dropdown
    const loginButton = document.getElementById('loginButton');
    const loginOptions = document.getElementById('loginOptions');

    loginButton.addEventListener('click', function() {
        // Toggle visibility of dropdown
        if (loginOptions.style.display === 'block') {
            loginOptions.style.display = 'none';
        } else {
            loginOptions.style.display = 'block';
        }
    });

    // Close the dropdown if the user clicks outside of it
    window.addEventListener('click', function(event) {
        if (!event.target.matches('#loginButton')) {
            if (loginOptions.style.display === 'block') {
                loginOptions.style.display = 'none';
            }
        }
    });

    window.onclick = function(event) {
        if (!event.target.matches('#loginButton')) {
            var options = document.getElementById('loginOptions');
            options.style.display = 'none';
        }
    };

    function redirectTo(page) {
        window.location.href = page;
    }

    function updateVisitorText() {
        const visitorTextElement = document.getElementById('visitorText');
        if (!visitorTextElement) {
            console.error('Element with id visitorText not found!');
            return;
        }
        if (currentLanguage === 'ar') {
            visitorTextElement.textContent = 'عدد المسجلين'; // النص بالعربية
        } else {
            visitorTextElement.textContent = 'Number of Registrants'; // النص بالإنجليزية
        }
    }

    // Circular Scrolling Carousel for Partners Section
    const partnersCarousel = document.querySelector('.partners-wrapper');
    const partnerCards = partnersCarousel.children;
    let currentIndex = 0;
    const totalCards = partnerCards.length;

    // Duplicate the first few cards to create a seamless effect
    for (let i = 0; i < totalCards; i++) {
        const clone = partnerCards[i].cloneNode(true);
        partnersCarousel.appendChild(clone);
    }

    function updateCarousel() {
        const offset = -currentIndex * partnerCards[0].clientWidth;
        partnersCarousel.style.transition = 'transform 0.5s ease';
        partnersCarousel.style.transform = `translateX(${offset}px)`;
        currentIndex++;
        if (currentIndex >= totalCards) {
            currentIndex = 0;
        }
    }

    setInterval(updateCarousel, 3000); // Change image every 3 seconds

    // Automatic Carousel for Partners Section
    const partnersCarouselAuto = document.querySelector('.partners-wrapper');
    const partnerCardsAuto = partnersCarouselAuto.children;
    let currentIndexAuto = 0;

    function updateCarouselAuto() {
        currentIndexAuto = (currentIndexAuto + 1) % partnerCardsAuto.length;
        const offset = -currentIndexAuto * partnerCardsAuto[0].clientWidth;
        partnersCarouselAuto.style.transform = `translateX(${offset}px)`;
    }

    setInterval(updateCarouselAuto, 3000); // Change image every 3 seconds

    // Initialize the carousel with the first image
    updateCarouselAuto();

    // يمكنك إضافة وظيفة لتغيير اللغة هنا
    function changeLanguage(language) {
        currentLanguage = language;
        localStorage.setItem('language', currentLanguage);
        updateVisitorText(); // تحديث النص عند تغيير اللغة
        updateContent(); // تحديث المحتوى عند تغيير اللغة
        updateTextDirection(); // تحديث اتجاه النص عند تغيير اللغة
        updateButtonText(); // تحديث نص الزر عند تغيير اللغة
    }
});
